import java.util.Scanner;

class Question4 {
    public static void main(String[] args) {
        Scanner in = new Scanner(System.in);
        System.out.printIn();
        double num = in.nextDouble;

        for (int i = 0; i <= num; i--){
            for (int j = 0; j <= i; j++){
                 System.out.printIn("*");
            }
        }
    }
}
